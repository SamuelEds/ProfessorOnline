package bean;

public class resultadoBEAN {
    private int matricula;
    private String nome;
    private String serie;
    private String disciplina;
    private float bimestre_um;
    private float bimestre_dois;
    private float bimestre_tres;
    private float bimestre_quatro;
    private float media;
    private String resultado;

    public int getMatricula() {
        return matricula;
    }

    public void setMatricula(int matricula) {
        this.matricula = matricula;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSerie() {
        return serie;
    }

    public void setSerie(String serie) {
        this.serie = serie;
    }

    public String getDisciplina() {
        return disciplina;
    }

    public void setDisciplina(String disciplina) {
        this.disciplina = disciplina;
    }

    public float getBimestre_um() {
        return bimestre_um;
    }

    public void setBimestre_um(float bimestre_um) {
        this.bimestre_um = bimestre_um;
    }

    public float getBimestre_dois() {
        return bimestre_dois;
    }

    public void setBimestre_dois(float bimestre_dois) {
        this.bimestre_dois = bimestre_dois;
    }

    public float getBimestre_tres() {
        return bimestre_tres;
    }

    public void setBimestre_tres(float bimestre_tres) {
        this.bimestre_tres = bimestre_tres;
    }

    public float getBimestre_quatro() {
        return bimestre_quatro;
    }

    public void setBimestre_quatro(float bimestre_quatro) {
        this.bimestre_quatro = bimestre_quatro;
    }

    public float getMedia() {
        return media;
    }

    public void setMedia(float media) {
        this.media = media;
    }

    public String getResultado() {
        return resultado;
    }

    public void setResultado(String resultado) {
        this.resultado = resultado;
    }
    
    
}
